  J1=J(1,1); J2=J(2,2);J3=J(3,3);
  da = -J -h/2*[0, (J2-J3)*a(3), (J2-J3)*a(2);
                (J3-J1)*a(3), 0, (J3-J1)*a(1);
                (J1-J2)*a(2), (J1-J2)*a(1), 0] - h*h/4*((a'*(J*a))*eye(3) + 2*a*(J*a)');
  
  db = J -h/2*[0, (J2-J3)*b(3), (J2-J3)*b(2);
               (J3-J1)*b(3), 0, (J3-J1)*b(1);
               (J1-J2)*b(2), (J1-J2)*b(1), 0] + h*h/4*((b'*(J*b))*eye(3) + 2*b*(J*b)');
  
  